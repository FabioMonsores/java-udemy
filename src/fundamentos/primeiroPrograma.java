//ENTENDENDO MAIS SOBRE JAVA
//===============================
/*
 * Um programa JAVA é organizado por uma sentença de código.
 * E uma sentença de código é sempre terminada com ponto e vírgula (;)
 * O que irá determinar uma sentença de código é o término em ponto e vírgula (;)
 * Uma sentença de código não está associada à quantidade de linhas e sim com ponto e vírgula (;).
 * Um par de chaves ({}) em java quer dizer um bloco de código, posso ter múltiplas linhas e sentenças em um bloco de código.
 * Um bloco de código sempre irá envolver um outro bloco de código menos, NÃO existe um bloco onde serja a interseção de outro bloco. Sempre haverá espaçamento na criação de um bloco novo, para dar um "ar" de hierarquia.
 * O "main" é a porta de entrada de um programa JAVA. Podemos ter múltiplos "main", mas apenas um deverá ser usado para a execução do programa.
 */

